#ifndef PALINDROME_H
#define PALINDROME_H

#include <stddef.h>

/**
 * is_palindrome - Checks if a given unsigned long integer is a palindrome.
 * @n: The number to check.
 *
 * Description:
 * A palindrome number reads the same backward and forward.
 * This function checks whether the given number is a palindrome
 * without using dynamic memory allocation.
 *
 * Return: 1 if n is a palindrome, 0 otherwise.
 */
int is_palindrome(unsigned long n);


#endif
